CREATE TABLE SALA (
    NUMERO_SALA INT NOT NULL,
    QUANTIDADE_MAX INT NOT NULL,
    QUANTIDADE_ATUAL INT,
    PRIMARY KEY (NUMERO_SALA)
);

CREATE TABLE ALUNO (
    MATRICULA_ALUNO VARCHAR(6) NOT NULL,
    NOME_ALUNO VARCHAR(50) NOT NULL,
    IDADE_ALUNO INT NOT NULL,
    NUMERO_SALA INT,
    PRIMARY KEY (MATRICULA_ALUNO),
    FOREIGN KEY (NUMERO_SALA) REFERENCES SALA(NUMERO_SALA)
);

CREATE TABLE PROFESSOR (
    ID_PROFESSOR VARCHAR(6) NOT NULL,
    NOME_PROFESSOR VARCHAR(50),
    IDADE_PROFESSOR INT NOT NULL,
    DISCIPLINA_PROFESSOR VARCHAR(20),
    PRIMARY KEY (ID_PROFESSOR)
);

CREATE TABLE SALA_PROFESSOR (
    ID_PROFESSOR VARCHAR(6),
    NUMERO_SALA INT,
    PRIMARY KEY (ID_PROFESSOR, NUMERO_SALA),
    FOREIGN KEY (ID_PROFESSOR) REFERENCES PROFESSOR(ID_PROFESSOR),
    FOREIGN KEY (NUMERO_SALA) REFERENCES SALA(NUMERO_SALA)
);

